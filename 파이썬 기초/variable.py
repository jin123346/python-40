#자료형의 값을 저장하는 공간, 변수

#변수란? 
#변수는 데이터를 저장하는 공간에 이름을 붙인 것
#변수는 데이터를 저장하고 필요할 때마다 꺼내서 사용할 수 있게 해준다.   
#변수는 데이터를 저장하는 공간에 붙인 이름이다.
#변수는 데이터를 저장하는 공간의 이름이다.

a=[1,2,3] #리스트 자료형을 변수 a에 저장
#리스트 자료형(객체)이 자동으로 메모리에 생성되고, 변수 a가 그 메모리 공간을 가리킨다.
print(a)  #출력: [1, 2, 3]
id(a)    #변수 a가 가리키는 메모리 주소 확인
#출력 결과
# [1, 2, 3]
#4303029896  (메모리 주소는 실행할 때마다 다를 수 있음) 

#리스트를 복사할때 
b=a  #변수 a가 가리키는 메모리 주소를 변수 b에 복사
print(b)  #출력: [1, 2, 3]
id(b)    #변수 b가 가리키는 메모리 주소 확인
#출력 결과
#4303029896  (a의 메모리 주소와 동일)

AisB=a is b  #변수 a와 b가 동일한 객체를 가리키는지 확인
print("a와 b는 동일한 객체를 가리키는가?:", AisB)  #출력: a와 b는 동일한 객체를 가리키는가?: True
#출력 결과
# a와 b는 동일한 객체를 가리키는가?: True

a=[4,5,6]  #변수 a에 새로운 리스트 객체 할당
b=[4,5,6]  #변수 b에 새로운 리스트 객체 할당
AisB=a is b  #변수 a와 b가 동일한 객체를 가리키는지 확인
print("a와 b는 동일한 객체를 가리키는가?:", AisB)  #출력: a와 b는 동일한 객체를 가리키는가?: False
#출력 결과
# a와 b는 동일한 객체를 가리키는가?: False

#리스트 복사  : 슬라이싱 사용
a=[1,2,3]
b=a[:]  #변수 a가 가리키는 리스트를 슬라이싱하여 새로운 리스트 객체를 변수 b에 할당
a[1]=4  #변수 a가 가리키는 리스트의 두 번째 요소를 변경
print("a:", a)  #출력: a: [1, 4, 3]
print("b:", b)  #출력: b: [1, 2, 3]
#출력 결과
# a: [1, 4, 3]

#리스트 복사 copy() 메서드 사용

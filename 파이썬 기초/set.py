#집합자료형

# set() 함수로 집합 자료형 만들기
s1 = set([1,2,3])
print(s1)  # 출력: {1, 2, 3}
#출력 결과
# {1, 2, 3}

# 중복된 값이 있는 리스트로 집합 만들기
s2 = set([1,2,2,3,4,4,5])   
print(s2)  # 출력: {1, 2, 3, 4, 5}
#출력 결과
# {1, 2, 3, 4, 5}
# 집합에 값 추가하기
s1.add(4)
print(s1)  # 출력: {1, 2, 3, 4}
#출력 결과  
# {1, 2, 3, 4}
# 집합에서 값 제거하기
s1.remove(2)
print(s1)  # 출력: {1, 3, 4}
#출력 결과
# {1, 3, 4}
# 집합 연산: 합집합, 교집합, 차집합
s3 = set([3,4,5,6])
union = s1.union(s3)
intersection = s1.intersection(s3)
difference = s1.difference(s3)
print("합집합:", union)               # 출력: 합집합: {1, 3, 4, 5, 6}
print("교집합:", intersection)        # 출력: 교집합: {3, 4}
print("차집합:", difference)          # 출력: 차집합: {1}
#출력 결과
# 합집합: {1, 3, 4, 5, 6}
# 교집합: {3, 4}    
# 차집합: {1}
# 집합의 크기 구하기
size = len(s1)
print("집합의 크기:", size)  # 출력: 집합의 크기
#출력 결과
# 집합의 크기: 3
# 집합이 특정 값 포함 여부 확인하기
contains_3 = 3 in s1
print("3 포함 여부:", contains_3)  # 출력: 3 포함 여부: True
#출력 결과
# 3 포함 여부: True 

#교집합 채집합 합집합 등의 집합 연산 예제
A = set([1, 2, 3, 4])
B = set([3, 4, 5, 6])
# 교집합
intersection = A.intersection(B)
print("교집합:", intersection)  # 출력: 교집합: {3, 4}
# 합집합
union = A.union(B)
print("합집합:", union)        # 출력: 합집합: {1, 2, 3, 4, 5, 6}
# 차집합
difference = A.difference(B)    
print("차집합:", difference)    # 출력: 차집합: {1, 2}
#출력 결과
# 교집합: {3, 4}
# 합집합: {1, 2, 3, 4, 5, 6}
# 차집합: {1, 2}

# | 연산자와 & 연산자 사용 예제
A = set([1, 2, 3])
B = set([3, 4, 5])
# 합집합
union = A | B
print("합집합 (| 연산자):", union)  # 출력: 합집합 (| 연산자): {1, 2, 3, 4, 5}
# 교집합
intersection = A & B
print("교집합 (& 연산자):", intersection)  # 출력: 교집합 (& 연산자): {3}
#출력 결과  
# 합집합 (| 연산자): {1, 2, 3, 4, 5}
# 교집합 (& 연산자): {3}    

#값 추가 및 여러개 추가 예제
s = set([1, 2, 3])
s.add(4)
print("값 추가 후 집합:", s)  # 출력: 값 추가 후 집합: {1, 2, 3, 4}
s.update([5, 6, 7]) 
print("여러 값 추가 후 집합:", s)  # 출력: 여러 값 추가 후 집합: {1, 2, 3, 4, 5, 6, 7}
#출력 결과
# 값 추가 후 집합: {1, 2, 3, 4}
# 여러 값 추가 후 집합: {1, 2, 3, 4, 5, 6, 7}

#특정 값 제거하기
s = set([1, 2, 3, 4, 5])
s.remove(3)
print("값 제거 후 집합:", s)  # 출력: 값 제거 후 집합: {1, 2, 4, 5}
#출력 결과
# 값 제거 후 집합: {1, 2, 4, 5}


# 요약
#- 집합 자료형은 중복된 값을 허용하지 않는 자료형입니다.
#- 교집합 차집합 합집합 등의 집합 연산을 지원합니다.
#- set() 함수를 사용하여 집합을 만들 수 있습니다.
#- add() 메서드로 값을 추가하고, remove() 메서드로 값을 제거할 수 있습니다.
#- union(), intersection(), difference() 메서드로 집합 연산을 수행할 수 있습니다.
#- len() 함수로 집합의 크기를 구할 수 있습니다. 
#- in 연산자로 특정 값이 집합에 포함되어 있는지 확인할 수 있습니다.
